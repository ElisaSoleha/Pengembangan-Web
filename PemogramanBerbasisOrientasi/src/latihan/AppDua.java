package latihan;

public class AppDua {
    private String nama;
    private String nim;

    public AppDua(String nama, String nim){
        this.nama = nama;
        this.nim = nim;
    }
    public String getNama()
    {
        return nama;
    }
    public String getNim()
    {
        return nim;
    }
    public void setNama(String nama)
    {
        this.nama = nama;

    }
    public void setNim(String nim){
        this.nim = nim;
    }
    
}
